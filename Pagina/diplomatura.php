            <h2>Diplomatura Universitaria en Educación para la Paz y el Abordaje de la Conflictividad Social</h2>
            <h2>Síntesis de la propuesta pedagógica</h2>
            <p>En el año 2014 la Facultad de Ciencias Sociales de la UNICEN,  puso en marcha la Diplomatura Universitaria en Educación para la Paz y el abordaje de la conflictividad social en la Sede Quequén. Esta propuesta curricular resulta inédita tanto por la temática que aborda y el enfoque pedagógico que asume, como por el proceso de articulación entre universidad y sociedad que implicó su creación. 
            Si bien en las universidades se ha avanzado en la producción de conocimiento sobre conflicto y violencia, la investigación para la paz y el estudio de la gestión pacífica del conflicto ha quedado postergada en los ámbitos académicos, particularmente desde el abordaje interdisciplinario. 
            La Diplomatura  recoge y potencia el trabajo territorial realizado por una ONG local  y propone  la sistematización y formalización de un campo de conocimiento poco desarrollado en nuestro país y de gran interés a nivel social, educativo y político, reafirmando el compromiso y el involucramiento de la universidad con los problemas sociales.</p>
        
            <p>Cabe mencionar como antecedente de la Diplomatura, la articulación académica entre la Cátedra Abierta “Alfredo Marcenac” FACSO-QUEQUÉN (Resolución 3868 CS-UNCPBA) y el grupo de Estudios Socioculturales del Conflicto (GESC) que dirige el Dr. 
            Juan Pablo Matta (NURES-FACSO-UNCPBA, Sede Olavarría). La Cátedra abierta fue promovida por la Asociación Civil “Alfredo Marcenac” (ACAM) como espacio académico articulador de las actividades de la asociación y de la Universidad. 
            Por otra parte, la ACAM y la Cátedra Abierta vienen desarrollando experiencias en territorio escolar desde el año 2008[1] y cursos de formación con reconocimiento oficial[2] desde el 2010[3]. 
            Estas actividades están dirigidas a los diferentes actores de la comunidad educativa (alumnos de los espacios de la práctica de la formación docente terciaria, profesionales de la modalidad de psicología comunitaria y pedagogía social) de la región 20 de la Provincia de Buenos Aires en temáticas como la resolución alternativa de conflictos y la mediación escolar.</p>
        
            <p>Por su parte, desde una perspectiva antropológica, el GESC se encuentra trabajando en la temática de la mediación comunitaria para la resolución de la conflictividad vecinal desde el año 2011 y en la consolidación de un espacio de trabajo científico en la FACSO-UNCPBA. 
            Esta línea de investigación desarrolla actividades de campo en el ámbito del Centro Municipal de Relaciones Vecinales y Defensa del Consumidor de Olavarría.</p>
        
            <p>Son objetivos de la Diplomatura:</p>
            <ul>
                <li>Aportar a la comprensión de la complejidad de los procesos micro, meso y macrosociales que intervienen en la conflictividad social y en la construcción de paz, desde una mirada interdisciplinaria.</li>

                <li>Fortalecer en los participantes la adquisición de estrategias de gestión de situaciones conflictivas y la implementación de iniciativas para la construcción de la paz en los espacios sociales donde se desempeñan.</li>
            </ul>

            <p>La Diplomatura ya cuenta con 100 egresados A fines de 2023, la Universidad aprobó (Resolución 8563/23), el dictado de la misma a distancia. 
            Esta propuesta formativa con modalidad virtual, iniciará en marzo de 2025.</p>
        
